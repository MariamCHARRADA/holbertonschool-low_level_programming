#include "3-calc.h"
/**
 * op_add - adds two integers
 * @a: int
 * @b: int
 * Return: sum or NULL
 */
int op_add(int a, int b)
{
		return (a + b);
}
/**
 * op_sub - subtitute
 * @a: int
 * @b: int
 * Return: difference or NULL
 */
int op_sub(int a, int b)
{
		return (a - b);
}
/**
 * op_add - multiplies two integers
 * @a: int
 * @b: int
 * Return: product or NULL
 */
int op_mul(int a, int b)
{
		return (a * b);
}
/**
 * op_div - devision
 * @a: int
 * @b: int
 * Return: devision result or NULL
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - devision remainder
 * @a: int
 * @b: int
 * Return: remainder or NULL
 */
int op_mod(int a, int b)
{
	return (a % b);
}
